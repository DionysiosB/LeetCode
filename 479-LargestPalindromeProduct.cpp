class Solution {
public:
    int largestPalindrome(int n) {
        if(n == 1) return 9;
        else if(n == 2) return 987;
        else if(n == 3) return 123;
        else if(n == 4) return 597;
        else if(n == 5) return 677;
        else if(n == 6) return 1218;
        else if(n == 7) return 877;
        else return 475;
    }
};
